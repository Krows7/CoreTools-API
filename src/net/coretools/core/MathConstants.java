package net.coretools.core;

/**
 * 
 * MathConstants interface contains most recent numbers.
 * 
 * @since 1.4.9 (4'th patch)
 * 
 * @author Krows
 *
 */
public interface MathConstants {

/**
 * 
 * 0 "zero" number.
 * 
 */
	public static final long ZERO = 0L;
	
/**
 * 
 * 1 "one" number.
 * 
 */
	public static final long ONE = 1L;
	
/**
 * 
 * 10 "ten" number.
 * 
 */
	public static final long TEN = 10L;
	
/**
 * 
 * 100 "one hundred" number.
 * 
 */
	public static final long HUNDRED = 100L;
	
/**
 * 
 * 1,000 "one thousand" number.
 * 
 */
	public static final long THOUSAND = 1_000L;
	
/**
 * 
 * 1,000,000 "one million" number.
 * 
 */
	public static final long MILLION = 1_000_000L;
	
/**
 * 
 * 1,000,000,000 "one billion" number.
 * 
 */
	public static final long BILLION = 1_000_000_000L;
}