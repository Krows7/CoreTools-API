package net.coretools.core.time;

/**
 * 
 * TimeConstants interface contains most recent time converting constants.
 * 
 * @since 1.4.9 (4'th patch)
 * 
 * @author Krows
 *
 */
public interface TimeConstants {

/**
 * 
 * The number of converting one second to milliseconds.
 * 
 */
	public static final long SECOND_TO_MILLISECOND = 1_000;
	
/**
 * 
 * The number of converting one second to nanoseconds.
 * 
 */
	public static final long SECOND_TO_NANOSECOND = 1_000_000_000;
}